import Foundation

class HttpStatus {
    ///HTTP status | Int: 200 - Ok
    static let ok           : Int   = 200
    
    ///HTTP status | Int: 201 - Created
    static let created      : Int   = 201
    
    ///HTTP status | Int: 400 - Bad request
    static let badRequest   : Int   = 400
    
    ///HTTP status | Int: 401 - Unauthorized
    static let unauthorized : Int   = 401
    
    ///HTTP status | Int: 403 - Forbidden
    static let forbidden    : Int   = 403
    
    ///HTTP status | Int: 404 - Not found
    static let notFound     : Int   = 404
    
    ///HTTP status | Int: 409 - Conflict
    static let conflict     : Int   = 409
}
